numerouno= int (input("ingrese primer numero"))
numerodos= int (input("ingrese segundo numero"))
operacion= input ("eliga operador +,-,*,/")
if operacion=="+":
    resultado= numerouno + numerodos
    print(resultado)
if operacion=="-":
    resultado1= numerouno - numerodos
    print(resultado1)
if operacion=="*":
    resultado2=numerouno*numerodos
    print(resultado2)
if operacion=="/":
    resultado3= numerouno/numerodos
    print(resultado3)
    

